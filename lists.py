#Criando uma lista
my_list = [1, 'hello', 3.14, True]

#Acessando elemento da lista
print(my_list[1]) #Saida: hello

#Modificando elementos da lista
my_list[0] = 10

#Imprimindo o tamanho da lista
print(len(my_list)) #Saida: 4

#imprimindo a lista completa 
print(my_list) #Saida: 10, 'hello', 3.14, True